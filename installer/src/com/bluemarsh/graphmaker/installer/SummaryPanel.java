    /**
     * Populate the text pane with the latest information.
     */
    private void populateText() {
        StyledDocument doc = reviewTextPane.getStyledDocument();
        try {
            Style rstyle = doc.getStyle("regular");
            Style estyle = doc.getStyle("emphasized");
            String msg = Bundle.getString("MSG_Summary_Message");
            doc.insertString(doc.getLength(), msg, rstyle);
            msg = Bundle.getString("MSG_Summary_Location");
            doc.insertString(doc.getLength(), msg, rstyle);
            String home = Controller.getDefault().getProperty("home");
            doc.insertString(doc.getLength(), home, rstyle);
            msg = Bundle.getString("MSG_Summary_Executable");
            doc.insertString(doc.getLength(), msg, estyle);
            String exec = home + File.separator + "bin" + File.separator + "graphmaker";
            doc.insertString(doc.getLength(), exec, rstyle);
        } catch (BadLocationException ble) {
            // This would be entirely unexpected.
            ble.printStackTrace();
        }
    }

    @Override
    public void showPanel() {
        Controller.getDefault().markCompleted();
        populateText();
    }

